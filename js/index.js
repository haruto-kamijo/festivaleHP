function draw(){
  var canvas = document.getElementById('back');
  if (canvas.getContext){
    var ctx=canvas.getContext('2d');
    ctx.beginPath();
    ctx.fillStyle="rgba(0,0,0,1)";
    ctx.fillRect(350,0,100,700);
    ctx.fillRect(350,800,100,200)
    ctx.fill();
    ctx.beginPath();
    ctx.fillStyle="rgba(255,0,255,0.3)";
    ctx.arc(180,960,150,0,Math.PI*2);
    ctx.fill();
    ctx.beginPath();
    ctx.fillStyle="rgba(100,255,100,0.4)";
    ctx.arc(100,430,150,0,Math.PI*2);
    ctx.fill();
    ctx.beginPath();
    ctx.fillStyle="rgba(255,255,0,0.6)";
    ctx.arc(650,200,150,0,Math.PI*2);
    ctx.fill();
    ctx.beginPath();
    ctx.fillStyle="rgba(0,255,255,0.4)";
    ctx.arc(880,560,120,0,Math.PI*2);
    ctx.fill();
    ctx.beginPath();
    ctx.fillStyle="rgba(0,0,255,0.3)";
    ctx.arc(1600,330,150,0,Math.PI*2);
    ctx.fill();
    ctx.beginPath();
    ctx.fillStyle="rgba(255,0,0,0.3)";
    ctx.arc(1350,90,150,0,Math.PI*2);
    ctx.fill();
    ctx.beginPath();
    ctx.fillStyle="rgba(0,255,0,0.3)";
    ctx.arc(1150,950,150,0,Math.PI*2);
    ctx.fill();
    ctx.beginPath();
    ctx.strokeStyle="rgba(0,0,0,1)";
    ctx.lineWidth=2.3;
    ctx.moveTo(2200,700);
    ctx.lineTo(0,700);
    ctx.lineTo(200,800);
    ctx.lineTo(2200,800);
    ctx.closePath();
    ctx.stroke();
    ctx.beginPath();
    ctx.lineWidth=2;
    ctx.moveTo(1150,300);
    ctx.lineTo(800,300);
    ctx.stroke();
    ctx.beginPath();
    ctx.lineWidth=2;
    ctx.moveTo(900,200);
    ctx.lineTo(900,400);
    ctx.stroke();
    ctx.beginPath();
    ctx.lineWidth=2;
    ctx.moveTo(1350,350);
    ctx.lineTo(1350,500);
    ctx.stroke();
    ctx.beginPath();
    ctx.lineWidth=2;
    ctx.moveTo(1150,450);
    ctx.lineTo(1400,450);
    ctx.stroke();
  }
}
